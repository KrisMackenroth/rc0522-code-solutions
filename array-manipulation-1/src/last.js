/* exported last */
// create a new variable to assign the last index of the array to.
// acquire the length of the array and minus one from it to locate the last index.
// assign the new variable to the last index.
// return the new variable.
function last(array) {
  var lastIndex;
  lastIndex = array[array.length - 1];
  return lastIndex;
}

