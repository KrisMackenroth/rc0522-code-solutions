function isVowel(char) {
  if (char === 'A' || char === 'a' || char === 'E' || char === 'e' || char === 'I' || char === 'i' || char === 'O' || char === 'o' || char === 'U' || char === 'u' || char === 'Y' || char === 'y' || char === 'W' || char === 'w') {
    return true;
  }
  return false;
}

